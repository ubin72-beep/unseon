/* =====================================================
   10. 사용자 입력 파싱
   ===================================================== */
function parseBirthInfo(text) {
  const result = { year:null, month:null, day:null, hour:-1, min:0, isLunar:false, isLeap:false, found:false };
  if (!text) return result;

  if (/음력|陰曆/.test(text)) result.isLunar = true;
  if (/윤달|윤월|閏月/.test(text)) result.isLeap = true;

  const yearMatch = text.match(/(\d{4})\s*년?/);
  if (yearMatch) result.year = parseInt(yearMatch[1]);

  const monthMatch = text.match(/(\d{1,2})\s*월/);
  if (monthMatch) result.month = parseInt(monthMatch[1]);

  const dayMatch = text.match(/(\d{1,2})\s*일/);
  if (dayMatch) result.day = parseInt(dayMatch[1]);

  if (!result.year) {
    const numMatch = text.match(/(\d{4})[.\-\/](\d{1,2})[.\-\/](\d{1,2})/);
    if (numMatch) {
      result.year  = parseInt(numMatch[1]);
      result.month = parseInt(numMatch[2]);
      result.day   = parseInt(numMatch[3]);
    }
  }

  const minMatch = text.match(/(\d{1,2})\s*분/);
  if (minMatch) result.min = parseInt(minMatch[1]);

  const timeMatch = text.match(/(\d{1,2}):(\d{2})/);
  if (timeMatch) {
    result.hour = parseInt(timeMatch[1]);
    result.min  = parseInt(timeMatch[2]);
  }

  const hourPatterns = [
    { re: /자정|밤\s*12시/, h: 0 },
    { re: /새벽\s*(\d{1,2})\s*시/, group: 1, am: true },
    { re: /오전\s*(\d{1,2})\s*시/, group: 1, am: true },
    { re: /오후\s*(\d{1,2})\s*시/, group: 1, pm: true },
    { re: /밤\s*(\d{1,2})\s*시/,   group: 1, pm: true, night: true },
    { re: /(\d{1,2})\s*시/,         group: 1 },
  ];

  if (result.hour < 0) {
    for (let i = 0; i < hourPatterns.length; i++) {
      const p = hourPatterns[i];
      const m = text.match(p.re);
      if (m) {
        if (p.h !== undefined) {
          result.hour = p.h;
        } else {
          let h = parseInt(m[p.group]);
          if (p.pm && h < 12) h += 12;
          if (p.am && h === 12) h = 0;
          if (p.night && h >= 1 && h <= 6) h += 12;
          result.hour = h;
        }
        break;
      }
    }
  }

  const siNames = [
    { re: /야자시|夜子時/,  h: 23 },
    { re: /정자시|正子時/,  h: 0  },
    { re: /자시|子時/,      h: 0  },
    { re: /축시|丑時/,      h: 1  },
    { re: /인시|寅時/,      h: 3  },
    { re: /묘시|卯時/,      h: 5  },
    { re: /진시|辰時/,      h: 7  },
    { re: /사시|巳時/,      h: 9  },
    { re: /오시|午時/,      h: 11 },
    { re: /미시|未時/,      h: 13 },
    { re: /신시|申時/,      h: 15 },
    { re: /유시|酉時/,      h: 17 },
    { re: /술시|戌時/,      h: 19 },
    { re: /해시|亥時/,      h: 21 },
  ];
  if (result.hour < 0) {
    for (let i = 0; i < siNames.length; i++) {
      if (siNames[i].re.test(text)) { result.hour = siNames[i].h; break; }
    }
  }

  result.found = !!(result.year && result.month && result.day);
  return result;
}
