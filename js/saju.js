function getLunarYearDays(year) {
  const db = LUNAR_DB[year];
  if (!db) return 365;
  let total = 0;
  for (let m = 0; m < 12; m++) total += db.months[m];
  if (db.leapMonth && db.leapDays) total += db.leapDays;
  return total;
}

/**
 * ★ 날짜를 절대 일수(일련번호)로 변환
 * 기준: 양력 1900년 1월 31일 = 음력 1900년 1월 1일 = 절대일수 0
 * JDN(1900-01-31) = 2415051
 */
function _toAbsDay(y, m, d) {
  const Y = m <= 2 ? y - 1 : y;
  const M = m <= 2 ? m + 12 : m;
  const A = Math.floor(Y / 100);
  const B = 2 - A + Math.floor(A / 4);
  const jdn = Math.floor(365.25 * (Y + 4716)) + Math.floor(30.6001 * (M + 1)) + d + B - 1524;
  return jdn - 2415051;
}

/**
 * ★ 양력→음력 변환
 */
function solarToLunar(sy, sm, sd) {
  const targetAbs = _toAbsDay(sy, sm, sd);
  if (targetAbs < 0) return null;

  let lunarYear = 1900;
  for (let y = 1900; y <= 2060; y++) {
    const db = LUNAR_DB[y];
    if (!db) break;
    const chunjolAbs = _toAbsDay(y, db.chunjol_m, db.chunjol_d);
    if (targetAbs < chunjolAbs) {
      lunarYear = y - 1;
      break;
    }
    lunarYear = y;
  }

  const db = LUNAR_DB[lunarYear];
  if (!db) return null;
  const chunjolAbs = _toAbsDay(lunarYear, db.chunjol_m, db.chunjol_d);
  let offset = targetAbs - chunjolAbs;

  let lunarMonth = 1, lunarDay = 1, isLeap = false;
  for (let m = 1; m <= 12; m++) {
    const days = db.months[m - 1];
    if (offset < days) { lunarMonth = m; lunarDay = offset + 1; isLeap = false; break; }
    offset -= days;
    if (db.leapMonth === m && db.leapDays > 0) {
      if (offset < db.leapDays) { lunarMonth = m; lunarDay = offset + 1; isLeap = true; break; }
      offset -= db.leapDays;
    }
  }
  return { year: lunarYear, month: lunarMonth, day: lunarDay, isLeap, leapMonth: db.leapMonth };
}

/**
 * ★ 음력→양력 변환
 */
function lunarToSolar(ly, lm, ld, isLeap) {
  isLeap = isLeap || false;
  const db = LUNAR_DB[ly];
  if (!db) return null;

  let offset = _toAbsDay(ly, db.chunjol_m, db.chunjol_d);

  for (let m = 1; m < lm; m++) {
    offset += db.months[m - 1];
    if (db.leapMonth === m && db.leapDays > 0) offset += db.leapDays;
  }
  if (isLeap && db.leapMonth === lm) offset += db.months[lm - 1];
  offset += ld - 1;

  const jdn = offset + 2415051;
  const a = jdn + 32044;
  const b = Math.floor((4 * a + 3) / 146097);
  const c = a - Math.floor(146097 * b / 4);
  const dd = Math.floor((4 * c + 3) / 1461);
  const e = c - Math.floor(1461 * dd / 4);
  const mm = Math.floor((5 * e + 2) / 153);
  const day   = e - Math.floor((153 * mm + 2) / 5) + 1;
  const month = mm + 3 - 12 * Math.floor(mm / 10);
  const year  = 100 * b + dd - 4800 + Math.floor(mm / 10);
  return { year, month, day };
}

