const CHEONGAN_CONST = ['甲','乙','丙','丁','戊','己','庚','辛','壬','癸'];
const JIJI_CONST     = ['子','丑','寅','卯','辰','巳','午','未','申','酉','戌','亥'];

/**
 * ★ 연주 (年柱) — 입춘 시각 기준
 */
function getYearPillar(sy, sm, sd, sh, smin) {
  let year = sy;
  const ipchun = getJeolgiTime(sy, 1);
  if (!isAfterJeolgi(sy, sm, sd, sh, smin, ipchun)) {
    year = sy - 1;
  }
  // 1900=경자(庚子): 천간 6(庚), 지지 0(子)
  const offset = year - 1900;
  const cgIdx = ((offset % 10) + 6 + 10) % 10;
  const jjIdx = ((offset % 12) + 0 + 12) % 12;
  return {
    cg: CHEONGAN[cgIdx], jj: JIJI[jjIdx],
    cgKr: CHEONGAN_KR[cgIdx], jjKr: JIJI_KR[jjIdx],
    cgIdx, jjIdx, year
  };
}

/**
 * ★ 월주 (月柱) — 절기 시각 기준 (오호둔년법)
 */
function getMonthPillar(sy, sm, sd, sh, smin, yearCgIdx) {
  const curJeolgi = getJeolgiTime(sy, sm - 1);
  let sajuMon = sm;

  if (!isAfterJeolgi(sy, sm, sd, sh, smin, curJeolgi)) {
    sajuMon = sm - 1;
    if (sajuMon <= 0) sajuMon = 12;
  }

  // 월지: 양력 1월=丑(1), 2월=寅(2), ..., 12월=子(0)
  const jjIdx = sajuMon % 12;

  // 오호둔년법: 연간에 따른 인월 천간
  const monthCgBase = [2, 4, 6, 8, 0]; // 甲己, 乙庚, 丙辛, 丁壬, 戊癸
  const groupIdx = yearCgIdx % 5;
  const baseCg = monthCgBase[groupIdx];
  const inMonOffset = (sajuMon - 2 + 12) % 12;
  const cgIdx = (baseCg + inMonOffset) % 10;

  return {
    cg: CHEONGAN[cgIdx], jj: JIJI[jjIdx],
    cgKr: CHEONGAN_KR[cgIdx], jjKr: JIJI_KR[jjIdx],
    cgIdx, jjIdx, sajuMonth: sajuMon
  };
}

/**
 * 줄리안일수 계산
 */
function getJulianDay(y, m, d) {
  const a = Math.floor((14 - m) / 12);
  const yy = y + 4800 - a;
  const mm = m + 12 * a - 3;
  return d + Math.floor((153*mm+2)/5) + 365*yy + Math.floor(yy/4) - Math.floor(yy/100) + Math.floor(yy/400) - 32045;
}

/**
 * ★ 일주 (日柱) — 한국 만세력(KASI) 기준
 * 검증: 1973-03-09 = 甲辰日
 *   JDN=2441751, diff=26730, (26730%10+0)%10=0(甲), (26730%12+10)%12=4(辰) ✓
 */
function getDayPillar(sy, sm, sd) {
  const jd = getJulianDay(sy, sm, sd);
  const BASE_JD = 2415021;
  const diff = jd - BASE_JD;
  const cgIdx = ((diff % 10) + 0 + 10) % 10;
  const jjIdx = ((diff % 12) + 10 + 12) % 12;
  return {
    cg: CHEONGAN[cgIdx], jj: JIJI[jjIdx],
    cgKr: CHEONGAN_KR[cgIdx], jjKr: JIJI_KR[jjIdx],
    cgIdx, jjIdx
  };
}

/**
 * ★ 시주 (時柱) — 야자시(夜子時)/정자시(正子時) 구분
 */
function getTimePillar(hour, min, sy, sm, sd) {
  if (hour < 0) {
    return {
      cg:'?', jj:'?', cgKr:'미상', jjKr:'미상',
      cgIdx:-1, jjIdx:-1, timeName:'출생 시각 미입력', unknown:true
    };
  }

  min = min || 0;
  let dayForTime = { sy, sm, sd };
  let isYaJasi = false;

  if (hour === 23) {
    isYaJasi = true;
  } else if (hour === 0) {
    const prevDate = new Date(sy, sm - 1, sd - 1);
    dayForTime = {
      sy: prevDate.getFullYear(),
      sm: prevDate.getMonth() + 1,
      sd: prevDate.getDate()
    };
  }

  let jjIdx;
  const tm = hour * 60 + (min || 0);

  if      (tm >= 23*60 || tm < 1*60)    jjIdx = 0;  // 子
  else if (tm >= 1*60  && tm < 3*60)    jjIdx = 1;  // 丑
  else if (tm >= 3*60  && tm < 5*60)    jjIdx = 2;  // 寅
  else if (tm >= 5*60  && tm < 7*60)    jjIdx = 3;  // 卯
  else if (tm >= 7*60  && tm < 9*60)    jjIdx = 4;  // 辰
  else if (tm >= 9*60  && tm < 11*60)   jjIdx = 5;  // 巳
  else if (tm >= 11*60 && tm < 13*60)   jjIdx = 6;  // 午
  else if (tm >= 13*60 && tm < 15*60)   jjIdx = 7;  // 未
  else if (tm >= 15*60 && tm <= 17*60)  jjIdx = 8;  // 申
  else if (tm >  17*60 && tm <= 19*60)  jjIdx = 9;  // 酉
  else if (tm >  19*60 && tm <= 21*60)  jjIdx = 10; // 戌
  else                                   jjIdx = 11; // 亥

  const refDay = getDayPillar(dayForTime.sy, dayForTime.sm, dayForTime.sd);

  // 오자둔일법
  const timeCgBase = [0, 2, 4, 6, 8];
  const groupIdx = refDay.cgIdx % 5;
  const baseCg = timeCgBase[groupIdx];
  const cgIdx = (baseCg + jjIdx) % 10;

  const timeNames = ['자시(子時)','축시(丑時)','인시(寅時)','묘시(卯時)',
                     '진시(辰時)','사시(巳時)','오시(午時)','미시(未時)',
                     '신시(申時)','유시(酉時)','술시(戌時)','해시(亥時)'];

  return {
    cg: CHEONGAN[cgIdx], jj: JIJI[jjIdx],
    cgKr: CHEONGAN_KR[cgIdx], jjKr: JIJI_KR[jjIdx],
    cgIdx, jjIdx,
    timeName: (isYaJasi ? '야자시(夜子時) ' : '') + timeNames[jjIdx],
    hour, min, unknown: false,
    isYaJasi,
    refDayCgIdx: refDay.cgIdx
  };
}

/* =====================================================
   7. 오행 분석
   ===================================================== */
function analyzeOhaeng(pillars) {
  const count = [0, 0, 0, 0, 0];
  pillars.forEach(function(p) {
    if (p.cgIdx >= 0) count[CG_OHAENG[p.cgIdx]]++;
    if (p.jjIdx >= 0) count[JJ_OHAENG[p.jjIdx]]++;
  });
  const total = count.reduce((a, b) => a + b, 0) || 1;
  const pct = count.map(c => Math.round(c / total * 100));
  const dayOhaeng = CG_OHAENG[pillars[2].cgIdx] >= 0 ? CG_OHAENG[pillars[2].cgIdx] : 0;

  const SANGSAENG = [1, 2, 3, 4, 0];
  const SANGGEUK  = [2, 3, 4, 0, 1];

  const dayCount = count[dayOhaeng];
  const isStrong = dayCount >= 2;
  let yongsin, gisin;
  if (isStrong) {
    yongsin = SANGSAENG[dayOhaeng];
    gisin   = SANGGEUK[dayOhaeng];
  } else {
    yongsin = (dayOhaeng + 4) % 5;
    gisin   = SANGGEUK[dayOhaeng];
  }

  return { count, pct, dayOhaeng, yongsin, gisin, isStrong, ohaengNames: OHAENG, ohaengKrNames: OHAENG_KR };
}

/* =====================================================
   8. 메인 함수: 사주팔자 완전 계산
   ===================================================== */
function calcSaju(opts) {
  let sy, sm, sd;
  const sh   = (opts.hour !== undefined && opts.hour >= 0) ? opts.hour : -1;
  const smin = opts.min || 0;
  let lunarInfo = null, solarInfo = null;

  if (opts.isLunar) {
    const sol = lunarToSolar(opts.year, opts.month, opts.day, opts.isLeap || false);
    if (!sol) return { error: '음력 변환 실패' };
    sy = sol.year; sm = sol.month; sd = sol.day;
    solarInfo = sol;
    lunarInfo = { year: opts.year, month: opts.month, day: opts.day, isLeap: opts.isLeap };
  } else {
    sy = opts.year; sm = opts.month; sd = opts.day;
    solarInfo = { year: sy, month: sm, day: sd };
    lunarInfo = solarToLunar(sy, sm, sd);
  }

  const effectiveHour = sh >= 0 ? sh : -1;

  const yearP  = getYearPillar(sy, sm, sd, effectiveHour >= 0 ? effectiveHour : 12, smin);
  const monthP = getMonthPillar(sy, sm, sd, effectiveHour >= 0 ? effectiveHour : 12, smin, yearP.cgIdx);
  const dayP   = getDayPillar(sy, sm, sd);
  const timeP  = getTimePillar(effectiveHour, smin, sy, sm, sd);

  const pillars = [yearP, monthP, dayP, timeP];
  const ohaeng  = analyzeOhaeng(pillars);

  const ipchunInfo    = getJeolgiTime(sy, 1);
  const curJeolgiInfo = getJeolgiTime(sy, sm - 1);

  return {
    solar: solarInfo,
    lunar: lunarInfo,
    pillars: { year: yearP, month: monthP, day: dayP, time: timeP },
    ohaeng,
    jeolgi: { ipchun: ipchunInfo, curMonth: curJeolgiInfo },
    summary: {
      yearPillar:  yearP.cg  + yearP.jj  + '(' + yearP.cgKr  + yearP.jjKr  + ')',
      monthPillar: monthP.cg + monthP.jj + '(' + monthP.cgKr + monthP.jjKr + ')',
      dayPillar:   dayP.cg   + dayP.jj   + '(' + dayP.cgKr   + dayP.jjKr   + ')',
      timePillar:  timeP.unknown
        ? '미상(시각 미입력)'
        : timeP.cg + timeP.jj + '(' + timeP.cgKr + timeP.jjKr + ') ' + timeP.timeName,
      ohaengStr:  ohaeng.ohaengNames.map((n, i) => n + ':' + ohaeng.count[i]).join(' '),
      yongsinStr: OHAENG_KR[ohaeng.yongsin] + '(' + OHAENG[ohaeng.yongsin] + ')',
      gisinStr:   OHAENG_KR[ohaeng.gisin]   + '(' + OHAENG[ohaeng.gisin]   + ')',
      isStrong:   ohaeng.isStrong
    }
  };
}

